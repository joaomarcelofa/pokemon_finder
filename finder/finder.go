func FindPokemonOccurences(text string) []ti.Word {
	iterator := ti.NewTextIterator(text)

	pokemons := []ti.Word{}
	finish := false
	for !finish {
		word := iterator.Next()
		if word == nil {
			finish = true
			continue
		}

		if isPokemon(word.Text) {
			pokemons = append(pokemons, *word)
		}
	}

	return pokemons
}

